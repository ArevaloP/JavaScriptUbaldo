//While
let contador = 0;
while(contador < 3){
    console.log(contador);
    contador++;
}

console.log("Fin ciclo while.");

//Do While
contador = 0;
do{
    console.log(contador);
    contador++;
}while(contador < 3);
console.log("Fin del ciclo do while.");

//For
for(let cont = 0; cont < 3; cont++ ){
    console.log(cont);
}
console.log("Fin del ciclo for.");